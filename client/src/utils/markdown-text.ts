/**
 * 将 Markdown 文本预处理为纯文本，用于帖文列表「正文速览」等场景，
 * 避免把 #、**、`![](url)`、`[](url)`、代码围栏等原始语法直接展示给用户。
 *
 * 输入通常是后端截断过的正文片段（可能在语法中间被切断，例如残缺的
 * `![alt](https://...` 没有右括号），因此对残缺语法也做了兜底处理。
 * 该函数对已是纯文本的输入是幂等的（不会破坏正常文字）。
 */
export function stripMarkdown(input: string): string {
  if (!input) return "";
  return input
    // 代码围栏 ```...```（含被截断、缺少结尾的开头围栏）
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/```[^\n]*/g, " ")
    // 图片 ![alt](url) —— 整体丢弃；并兜底处理被截断的残缺图片
    .replace(/!\[[^\]]*\]\([^)]*\)/g, " ")
    .replace(/!\[[^\]]*\]?\([^)]*$/g, " ")
    .replace(/!\[[^\]]*$/g, " ")
    // 链接 [text](url) -> text
    .replace(/\[([^\]]*)\]\([^)]*$/g, "$1")
    .replace(/\[([^\]]*)\]\([^)]*\)/g, "$1")
    // 行内代码 `code` -> code
    .replace(/`([^`]+)`/g, "$1")
    .replace(/`/g, "")
    // 行首标记：标题 #、引用 >、列表项 -/*/+/1.
    .replace(/^[ \t]*#{1,6}[ \t]+/gm, "")
    .replace(/^[ \t]*>+[ \t]?/gm, "")
    .replace(/^[ \t]*([-*+]|\d+[.)])[ \t]+/gm, "")
    // 整行水平分割线
    .replace(/^[ \t]*([-*_])(?:[ \t]*\1){2,}[ \t]*$/gm, " ")
    // 强调 / 删除线标记
    .replace(/(\*\*|__|~~|\*|_)/g, "")
    // 残余 HTML 标签
    .replace(/<[^>]+>/g, " ")
    // 折叠所有空白（含换行）为单个空格
    .replace(/\s+/g, " ")
    .trim();
}
